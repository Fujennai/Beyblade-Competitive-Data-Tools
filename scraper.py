ratchets_especiales = ["Turbo", "Operate", "Zillion"]

def separar_componentes(texto, ratchets_especiales):
    partes = texto.split()

    ratchet_idx = None

    # Detectar ratchet (normal o especial)
    for i, p in enumerate(partes):
        if re.match(r"\d+-\d+", p) or p in ratchets_especiales:
            ratchet_idx = i
            break

    if ratchet_idx is None:
        return texto, None, None

    # Blade = todo antes
    blade = " ".join(partes[:ratchet_idx])

    # Ratchet
    ratchet = partes[ratchet_idx]

    # Bit (después de la X si existe)
    if "X" in partes:
        x_idx = partes.index("X")
        bit = " ".join(partes[x_idx + 1:]) if len(partes) > x_idx + 1 else None
    else:
        bit = " ".join(partes[ratchet_idx + 1:]) if len(partes) > ratchet_idx + 1 else None

    return blade, ratchet, bit


data = []
i = 1

while i < len(rows):

    cols = rows[i].find_all("td")
    if not cols:
        i += 1
        continue

    combo_text = cols[0].get_text(" ", strip=True)
    winrate_text = cols[1].get_text(strip=True)
    partidas_text = cols[2].get_text(strip=True)
    eficiencia_text = cols[3].get_text(strip=True)

    # --- Formato antiguo con • ---
    if "•" in combo_text:
        parte_principal, bit = combo_text.split("•", 1)
        bit = bit.strip()
    else:
        parte_principal = combo_text
        bit = None

    # --- Separación correcta ---
    blade, ratchet, bit_text = separar_componentes(parte_principal, ratchets_especiales)

    if ratchet is None:
        i += 2
        continue

    if bit is None:
        bit = bit_text

    # --- Regla ratchets especiales ---
    if ratchet in ratchets_especiales:
        if bit is None or bit == "":
            bit = ratchet
        else:
            i += 2
            continue

    # --- Validación básica ---
    if not es_combo_valido(blade, ratchet, bit):
        i += 2
        continue

    # --- Métricas ---
    win_percent = None
    wins = None
    losses = None

    win_match = re.search(r"([\d\.]+)%", winrate_text)
    wl_match = re.search(r"(\d+)W\s*-\s*(\d+)L", winrate_text)

    if win_match:
        win_percent = float(win_match.group(1))
    if wl_match:
        wins = int(wl_match.group(1))
        losses = int(wl_match.group(2))

    try:
        partidas = int(partidas_text)
    except:
        i += 2
        continue

    try:
        eficiencia = int(re.search(r"\d+", eficiencia_text).group())
    except:
        i += 2
        continue

    # --- Detalle (fila siguiente) ---
    detalle_text = ""
    if i + 1 < len(rows):
        detalle_text = rows[i + 1].get_text(" ", strip=True)

    nums = re.findall(r"\d+\.\d+", detalle_text)

    pts_ganados = float(nums[0]) if len(nums) > 0 else None
    pts_cedidos = float(nums[1]) if len(nums) > 1 else None

    data.append({
        "Blade": blade,
        "Ratchet": ratchet,
        "Bit": bit,
        "Win %": win_percent,
        "Wins": wins,
        "Losses": losses,
        "Partidas": partidas,
        "Eficiencia": eficiencia,
        "Pts Ganados/Combate": pts_ganados,
        "Pts Cedidos/Combate": pts_cedidos
    })

    i += 2


