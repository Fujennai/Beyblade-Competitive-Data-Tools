def separar_componentes(texto, ratchets_especiales):
    partes = texto.split()

    ratchet_idx = None

    for i, p in enumerate(partes):
        if re.match(r"\d+-\d+", p) or p in ratchets_especiales:
            ratchet_idx = i
            break

    if ratchet_idx is None:
        return texto, None, None

    # Blade = todo antes
    blade = " ".join(partes[:ratchet_idx])

    # Ratchet
    ratchet = partes[ratchet_idx]

    # Buscar la X (si existe)
    if "X" in partes:
        x_idx = partes.index("X")
        bit = " ".join(partes[x_idx + 1:]) if len(partes) > x_idx + 1 else None
    else:
        bit = " ".join(partes[ratchet_idx + 1:]) if len(partes) > ratchet_idx + 1 else None

    return blade, ratchet, bit

